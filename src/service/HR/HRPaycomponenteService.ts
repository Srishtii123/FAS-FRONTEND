import { dispatch } from 'store';
import { openSnackbar } from 'store/reducers/snackbar';
import { IApiResponse } from 'types/types.services';
import axiosServices from 'utils/axios';
import { TPaycomponent } from 'pages/HR/type/AddPaycontrollerHr.types';

class HRPaycomponent {
  //-------------Paycomponent---------------
  addHrPaycomponent = async (values: TPaycomponent) => {
    try {
      console.log(values);

      const response: IApiResponse<null> = await axiosServices.post('api/HR/gm/paycomponent', values);
      if (response.data.success) {
        dispatch(
          openSnackbar({
            open: true,
            message: response.data.message,
            variant: 'alert',
            alert: {
              color: 'success'
            },
            close: true
          })
        );
        return response.data.success;
      }
    } catch (error: unknown) {
      const knownError = error as { message: string };
      dispatch(
        openSnackbar({
          open: true,
          message: knownError.message,
          variant: 'alert',
          alert: {
            color: 'error'
          },
          severity: 'error',
          close: true
        })
      );
    }
  };
  editHrPaycomponent = async (values: TPaycomponent) => {
    try {
      ///${values.leave_type}
      const response: IApiResponse<null> = await axiosServices.put(`api/HR/gm/paycomponent`, values);
      if (response.data.success) {
        dispatch(
          openSnackbar({
            open: true,
            message: response.data.message,
            variant: 'alert',
            alert: {
              color: 'success'
            },
            close: true
          })
        );
        return response.data.success;
      }
    } catch (error: unknown) {
      const knownError = error as { message: string };
      dispatch(
        openSnackbar({
          open: true,
          message: knownError.message,
          variant: 'alert',
          alert: {
            color: 'error'
          },
          severity: 'error',
          close: true
        })
      );
    }
  };
}
const hrPayComponentInstance = new HRPaycomponent();
export default hrPayComponentInstance;
